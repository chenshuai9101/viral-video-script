# 结构模板汇总
STRUCTURE_TEMPLATES = {
    "douyin": DOUYIN_GOLDEN_STRUCTURE,
    "xiaohongshu": XIAOHONGSHU_PLANTING_STRUCTURE,
    "kuaishou": KUAISHOU_SHARE_STRUCTURE,
    "knowledge": KNOWLEDGE_STRUCTURE,
    "plot": PLOT_STRUCTURE,
}

def get_title_templates(style: str = None) -> list:
    """获取标题模板"""
    if style:
        return TITLE_TEMPLATES.get(style, TITLE_TEMPLATES["数字"])
    return TITLE_TEMPLATES

def get_hook_templates(style: str = None) -> list:
    """获取钩子模板"""
    if style:
        return HOOK_TEMPLATES.get(style, HOOK_TEMPLATES["震惊"])
    return HOOK_TEMPLATES

def get_structure_template(platform: str) -> dict:
    """获取内容结构模板"""
    return STRUCTURE_TEMPLATES.get(platform.lower(), DOUYIN_GOLDEN_STRUCTURE)

def fill_template(template: str, **kwargs) -> str:
    """填充模板"""
    result = template
    for key, value in kwargs.items():
        result = result.replace(f"{{{key}}}", str(value))
    return result
